def main():
    print(f"Connecting to Supabase...")
    supabase = create_client(SUPABASE_URL, SUPABASE_KEY)
    print(f"Connected!\n")

    # First, list all files actually in the storage bucket
    # to build a case-insensitive lookup map
    print("Fetching file list from Supabase Storage...")
    stored_files = {}
    try:
        # List files in the moti_photos folder
        response = supabase.storage.from_(BUCKET_NAME).list(STORAGE_FOLDER)
        for item in response:
            name = item.get("name", "")
            if name:
                stored_files[name.lower()] = name  # lowercase key -> actual name
        print(f"Found {len(stored_files)} files in storage.\n")
    except Exception as e:
        print(f"ERROR listing storage files: {e}")
        return

    updated   = 0
    not_found = 0
    failed    = 0
    total     = len(CAKE_MAP)

    for i, (handle, filename) in enumerate(sorted(CAKE_MAP.items(),
        key=lambda x: int(x[0].replace('cake-', '')) if x[0].replace('cake-', '').isdigit() else 9999), 1):

        prefix = f"[{i:4d}/{total}] {handle:<12}"

        # Find the actual filename in storage (case-insensitive)
        actual_filename = stored_files.get(filename.lower())

        if not actual_filename:
            not_found += 1
            print(f"{prefix} ✗ NOT IN STORAGE: {filename}")
            continue

        # Build the storage path and get public URL
        storage_path = f"{STORAGE_FOLDER}/{actual_filename}"
        try:
            public_url = supabase.storage.from_(BUCKET_NAME).get_public_url(storage_path)

            # Update products table
            result = supabase.table("products").update({"image": public_url}).eq("handle", handle).execute()

            updated += 1
            print(f"{prefix} ✓ -> {public_url[-60:]}")

        except Exception as e:
            failed += 1
            print(f"{prefix} ✗ ERROR: {e}")

        # Throttle slightly every 100 to avoid rate limits
        if i % 100 == 0:
            print(f"\n  --- Progress: {updated} updated, {not_found} not in storage, {failed} errors ---\n")
            time.sleep(0.5)

    print(f"\n{'='*60}")
    print(f"  COMPLETE!")
    print(f"  Updated in DB    : {updated}")
    print(f"  Not in storage   : {not_found}")
    print(f"  Errors           : {failed}")
    print(f"  Total cakes      : {total}")
    print(f"{'='*60}")

    if not_found > 0:
        print(f"\nNOTE: {not_found} files were not found in the storage bucket.")
        print(f"These images may have different filenames in storage.")
        print(f"Check the Supabase Storage UI for actual filenames.")


if __name__ == "__main__":
    main()