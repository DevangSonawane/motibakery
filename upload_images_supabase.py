def find_file(folder, filename):
    """Case-insensitive file finder"""
    direct = os.path.join(folder, filename)
    if os.path.exists(direct):
        return direct
    try:
        files = {f.lower(): f for f in os.listdir(folder)}
        actual = files.get(filename.lower())
        if actual:
            return os.path.join(folder, actual)
    except:
        pass
    return None

def main():
    print(f"Connecting to Supabase...")
    supabase = create_client(SUPABASE_URL, SUPABASE_KEY)
    print(f"Connected!\n")

    if not os.path.isdir(LOCAL_FOLDER):
        print(f"ERROR: Folder not found: {LOCAL_FOLDER}")
        print(f"Make sure you're running this from your motibakery/ directory")
        print(f"Expected: motibakery/converted_webp/")
        return

    uploaded  = 0
    skipped   = 0
    failed    = 0
    total     = len(CAKE_MAP)

    for i, (handle, webp_file) in enumerate(
        sorted(CAKE_MAP.items(), key=lambda x: int(x[0].replace('cake-','')) if x[0].replace('cake-','').isdigit() else 9999), 1
    ):
        prefix = f"[{i:4d}/{total}] {handle:<12}"

        file_path = find_file(LOCAL_FOLDER, webp_file)
        if not file_path:
            failed += 1
            print(f"{prefix} ✗ FILE NOT FOUND: {webp_file}")
            continue

        storage_path = f"{STORAGE_FOLDER}/{webp_file}"

        try:
            with open(file_path, 'rb') as f:
                data = f.read()

            # Upload (upsert=true = safe to re-run)
            supabase.storage.from_(BUCKET_NAME).upload(
                path=storage_path,
                file=data,
                file_options={"content-type": "image/webp", "upsert": "true"}
            )

            # Get public URL
            public_url = supabase.storage.from_(BUCKET_NAME).get_public_url(storage_path)

            # Update products table
            supabase.table("products").update({"image": public_url}).eq("handle", handle).execute()

            uploaded += 1
            print(f"{prefix} ✓ {webp_file}")

        except Exception as e:
            err = str(e)
            if "already exists" in err.lower():
                # File exists — just update the URL
                try:
                    public_url = supabase.storage.from_(BUCKET_NAME).get_public_url(storage_path)
                    supabase.table("products").update({"image": public_url}).eq("handle", handle).execute()
                    skipped += 1
                    print(f"{prefix} ~ already exists, URL updated")
                except Exception as e2:
                    failed += 1
                    print(f"{prefix} ✗ ERROR (exists but URL failed): {e2}")
            else:
                failed += 1
                print(f"{prefix} ✗ ERROR: {e}")

        # Rate limit safety
        if i % 100 == 0:
            print(f"\n  --- {uploaded} uploaded | {skipped} re-linked | {failed} failed ---\n")
            time.sleep(1)

    print(f"\n{'='*60}")
    print(f"  DONE!")
    print(f"  Uploaded & linked : {uploaded}")
    print(f"  Re-linked (existed): {skipped}")
    print(f"  Failed/not found   : {failed}")
    print(f"  Total              : {total}")
    print(f"{'='*60}")

if __name__ == "__main__":
    main()